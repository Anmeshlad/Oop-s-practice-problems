package BasicDemos;

public class AreaAndPerimeterOfCricle {

	public static void main(String[] args) {
		int r=5;
		double pi=3.142,area,perimeter;
		//Area of circle
		area=pi*r*r;
		System.out.println("Area of circle : " +area);
		
		//perimeter of circle
		perimeter=2*pi*r;
		System.out.println("Perimeter of circle : "+perimeter);

	}

}
